"""Unit tests for ``scripts/backfill_contracts.py`` (OMN-10076).

Coverage:
  1. Stubbed Linear client → generates skeleton YAML for a missing ticket.
  2. Existing ``contracts/OMN-XXXX.yaml`` → skipped (idempotent).
  3. Generated YAML round-trips through ``ModelTicketContract`` (OCC model).
  4. Missing ``LINEAR_API_KEY`` → nonzero exit + required error message.
  5. ``--range OMN-123:OMN-200`` → sweep limited to that ID range.
"""

from __future__ import annotations

import importlib
import os
import sys
from typing import TYPE_CHECKING, Any
from unittest.mock import MagicMock, patch

if TYPE_CHECKING:
    from pathlib import Path
    from types import ModuleType

import pytest
import yaml

# sys.path is extended by tests/unit/scripts/conftest.py — no local bootstrap needed.

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _make_linear_ticket(ticket_id: str = "OMN-150") -> dict[str, Any]:
    """Return a minimal Linear-API-shaped ticket dict."""
    return {
        "id": ticket_id,
        "identifier": ticket_id,
        "title": f"Test ticket {ticket_id}",
        "description": "Auto-generated by backfill test fixture",
        "state": {"name": "Todo"},
        "team": {"key": "OMN"},
    }


def _stub_linear_client(ticket_data: dict[str, Any]) -> MagicMock:
    """Return a MagicMock that mimics the Linear client used by the script."""
    client = MagicMock()
    client.get_issue.return_value = ticket_data
    return client


def _import_backfill() -> ModuleType:
    """Import backfill_contracts from the scripts directory.

    If the module is already cached (e.g., from a previous test in the same
    session), reload it so each test gets a clean state.
    """
    module_name = "backfill_contracts"
    if module_name in sys.modules:
        return importlib.reload(sys.modules[module_name])
    return importlib.import_module(module_name)


# ---------------------------------------------------------------------------
# Tests
# ---------------------------------------------------------------------------


def _stub_generate_skeleton_contract(
    ticket_id: str, summary: str, **_kwargs: Any
) -> str:
    """Return a minimal skeleton YAML for testing (avoids OMNI_HOME dependency)."""
    return (
        f"schema_version: '1.0.0'\n"
        f"ticket_id: {ticket_id}\n"
        f"summary: {summary}\n"
        f"is_seam_ticket: false\n"
        f"interface_change: false\n"
        f"interfaces_touched: []\n"
        f"evidence_requirements: []\n"
        f"emergency_bypass:\n"
        f"  enabled: false\n"
        f"  justification: ''\n"
        f"  follow_up_ticket_id: ''\n"
    )


@pytest.mark.unit
def test_generates_skeleton_yaml_for_missing_ticket(tmp_path: Path) -> None:
    """A missing ticket → script writes a skeleton YAML to contracts/."""
    backfill = _import_backfill()

    contracts_dir = tmp_path / "contracts"
    contracts_dir.mkdir()
    ticket_id = "OMN-150"
    output_path = contracts_dir / f"{ticket_id}.yaml"

    # Confirm the file does not exist before the call.
    assert not output_path.exists()

    stub_client = _stub_linear_client(_make_linear_ticket(ticket_id))

    # Patch _load_generate_skeleton_contract to avoid OMNI_HOME dependency in CI.
    with patch.object(
        backfill,
        "_load_generate_skeleton_contract",
        return_value=_stub_generate_skeleton_contract,
    ):
        backfill.generate_for_ticket(
            ticket_id=ticket_id,
            contracts_dir=contracts_dir,
            linear_client=stub_client,
            dry_run=False,
        )

    assert output_path.exists(), f"Expected {output_path} to be created"
    content = output_path.read_text(encoding="utf-8")
    assert ticket_id in content


@pytest.mark.unit
def test_skips_existing_contract(tmp_path: Path) -> None:
    """If ``contracts/OMN-XXXX.yaml`` already exists, script must skip it."""
    backfill = _import_backfill()

    contracts_dir = tmp_path / "contracts"
    contracts_dir.mkdir()
    ticket_id = "OMN-150"
    output_path = contracts_dir / f"{ticket_id}.yaml"

    # Pre-write a sentinel contract so the script sees an existing file.
    sentinel_content = "schema_version: '1.0.0'\nticket_id: OMN-150\n"
    output_path.write_text(sentinel_content, encoding="utf-8")
    original_mtime = output_path.stat().st_mtime

    stub_client = _stub_linear_client(_make_linear_ticket(ticket_id))

    result = backfill.generate_for_ticket(
        ticket_id=ticket_id,
        contracts_dir=contracts_dir,
        linear_client=stub_client,
        dry_run=False,
    )

    # File must not be overwritten (mtime unchanged, content unchanged).
    assert output_path.stat().st_mtime == original_mtime, (
        "File was modified — idempotency broken"
    )
    assert output_path.read_text(encoding="utf-8") == sentinel_content
    # The return value must signal a skip.
    assert result == "skipped", f"Expected 'skipped', got {result!r}"


