@pytest.mark.unit
def test_missing_linear_api_key_exits_nonzero(
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
    capsys: pytest.CaptureFixture[str],
) -> None:
    """Without LINEAR_API_KEY the script must exit nonzero with a clear message."""
    backfill = _import_backfill()

    contracts_dir = tmp_path / "contracts"
    contracts_dir.mkdir()

    # Remove only LINEAR_API_KEY — keep all other env vars intact so that
    # PATH, HOME, TMPDIR and other system variables remain available.
    # Using monkeypatch.delenv with raising=False is safe when the key may
    # not be present in the first place.
    monkeypatch.delenv("LINEAR_API_KEY", raising=False)

    with pytest.raises(SystemExit) as exc_info:
        backfill.main(
            [
                "backfill_contracts.py",
                "--range",
                "OMN-148:OMN-152",
                "--contracts-dir",
                str(contracts_dir),
            ]
        )

    assert exc_info.value.code != 0, (
        "Expected nonzero exit when LINEAR_API_KEY is absent"
    )
    captured = capsys.readouterr()
    combined = captured.out + captured.err
    assert "LINEAR_API_KEY" in combined, (
        "Error output must mention LINEAR_API_KEY; got: " + combined
    )
    assert "required" in combined.lower(), (
        "Error output must indicate it is required; got: " + combined
    )


@pytest.mark.unit
def test_range_argument_limits_sweep(tmp_path: Path) -> None:
    """``--range OMN-123:OMN-200`` must restrict processing to IDs 123-200."""
    backfill = _import_backfill()

    contracts_dir = tmp_path / "contracts"
    contracts_dir.mkdir()

    # Track which ticket IDs the script attempts to process.
    processed_ids: list[str] = []

    def _capturing_generate(ticket_id: str, **_kwargs: Any) -> str:
        processed_ids.append(ticket_id)
        return "generated"

    stub_client = MagicMock()

    with (
        patch.dict(os.environ, {"LINEAR_API_KEY": "test-key-stub"}),
        patch.object(backfill, "generate_for_ticket", side_effect=_capturing_generate),
        patch.object(backfill, "_build_linear_client", return_value=stub_client),
    ):
        backfill.main(
            [
                "backfill_contracts.py",
                "--range",
                "OMN-123:OMN-200",
                "--contracts-dir",
                str(contracts_dir),
            ]
        )

    assert processed_ids, "No tickets were processed — range may not be wired"

    # All processed IDs must fall within [123, 200].
    processed_nums = set()
    for tid in processed_ids:
        prefix, num_str = tid.rsplit("-", 1)
        assert prefix == "OMN", f"Unexpected ticket prefix in {tid}"
        num = int(num_str)
        assert 123 <= num <= 200, f"{tid} is outside the declared range OMN-123:OMN-200"
        processed_nums.add(num)

    # The range covers 78 IDs (123..200 inclusive). Every ID in [123, 200]
    # must appear — an implementation that skips IDs would fail this check.
    expected = set(range(123, 201))
    missing = expected - processed_nums
    assert not missing, (
        "Range OMN-123:OMN-200 is incomplete — missing IDs: "
        + ", ".join(f"OMN-{n}" for n in sorted(missing)[:10])
        + (" ..." if len(missing) > 10 else "")
    )
